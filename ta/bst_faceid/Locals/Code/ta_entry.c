TEE_Result TA_InvokeCommandEntryPoint(void *session_id,
                                      uint32_t command_id,
                                      uint32_t param_types,
                                      TEE_Param params[4])
{
	(void)(session_id);
	(void)(params);
	(void)(param_types);

	char fid_encoder[ENCODER_FID_LENGH];
	size_t len;
	size_t data_sz;
	bst_feature_t store;
	struct InOutParams *p_data;
	struct InOutParams in_data;
	struct InOutParams out_data;
	TEE_Result res = TEE_SUCCESS;

	EMSG("%s enter", __func__);

	if ((TEE_PARAM_TYPE_GET(param_types, 0) != TEE_PARAM_TYPE_MEMREF_INOUT) ||
		(TEE_PARAM_TYPE_GET(param_types, 1) != TEE_PARAM_TYPE_NONE) ||
		(TEE_PARAM_TYPE_GET(param_types, 2) != TEE_PARAM_TYPE_NONE) ||
		(TEE_PARAM_TYPE_GET(param_types, 3) != TEE_PARAM_TYPE_NONE)) {
		EMSG("The param_types is error!");
		return TEE_ERROR_BAD_PARAMETERS;
	}
	
	memset((char *)&out_data, 0, sizeof(struct InOutParams));
	p_data = params[0].memref.buffer;
	memcpy(&in_data, p_data, sizeof(struct InOutParams));

	switch (command_id) {
		case CMD_FACE_ADD:
			EMSG("==========(CMD_FACE_ADD enter)==========");
			res = addFeature((char *)&in_data.feature_data, &out_data.fid, &in_data.token);
			if (res != TEE_SUCCESS) {
				EMSG("%s:%d  res=0x%x(%d) failed ", __func__, __LINE__, res, res);
				return res;
			}
			break;
		case CMD_FACE_REMOVE:
			EMSG("==========(CMD_FACE_REMOVE enter)==========");
			res = removeFeature(in_data.fid);
			if (res != TEE_SUCCESS) {
				EMSG("%s:%d  res=0x%x(%d) failed ", __func__, __LINE__, res, res);
				return res;
			}
			break;
		case CMD_FACE_COMPARE:
			EMSG("==========(CMD_FACE_COMPARE enter)==========");
			
			// TODO  get fid
			
	
			base64_encode((char *)&g_fid, MAX_FID_LENGH , fid_encoder);
			EMSG("fid=%d, fid=0x%08x, fid_encoder = %s\n", g_fid, g_fid, fid_encoder);
			
			len = ENCODER_FID_LENGH;
			data_sz = sizeof(bst_feature_t);
			res = read_raw_object(fid_encoder,len, (char *)&store, &data_sz);
			if (res != TEE_SUCCESS) {
				EMSG("%s:%d  res=0x%x(%d) failed ", __func__, __LINE__, res, res);
				return res;
			}
			res = ta_compare_feature(&store, &in_data.feature_data);
			if(res == 0)
			{
				memcpy(out_data.result, magic, sizeof(magic));
			} else {
				memset(out_data.result, 0, sizeof(magic));
			}
			break;
		case CMD_FACE_GET_CHALLENGE:
			EMSG("==========(CMD_FACE_GET_CHALLENGE enter)==========");
			out_data.challenge = get_challenge();
			if (out_data.challenge == 0) {
				EMSG("%s:%d  res=0x%x(%d) failed ", __func__, __LINE__, res, res);
				return res;
			}
			break;
		case CMD_FACE_TEST:
			EMSG("==========(CMD_FACE_TEST enter)==========");
			test_compare();
			break;
		default:
			EMSG("==========(default enter)==========");
			break;
	}

	memcpy(params[0].memref.buffer, &out_data, sizeof(struct InOutParams));
	params[0].memref.size = sizeof(struct InOutParams);
	EMSG("%s exit", __func__);

	return res;
}
