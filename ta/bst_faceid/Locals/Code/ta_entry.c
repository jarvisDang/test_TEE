int tt_hmac_sha256(uint8_t* data, uint32_t size_data,
                    uint8_t* key, uint32_t size_key,
                    uint8_t* mac)
{
	size_t maclen;
	TEE_ObjectHandle hmac_key = TEE_HANDLE_NULL;
	TEE_Result res = TEE_SUCCESS;
	
	TEE_Attribute attr;
	TEE_MemFill(&attr, 0, sizeof(attr));
	
	attr.attributeID = TEE_ATTR_SECRET_VALUE;
	attr.content.ref.buffer = key;
	attr.content.ref.length = size_key;

	res = TEE_PopulateTransientObject(hmac_key, &attr, 1);
	if (res != TEE_SUCCESS) {
		EMSG("%s:%d  res=0x%x(%d) failed ", __func__, __LINE__, res, res);
		return res;
	}
	
	return hamc_sha256(hmac_key, data, size_data, mac, &maclen);

}

int tt_crypto_memcmp(const uint8_t *mem_1, const uint8_t *mem_2, size_t nbr_bytes) {
  size_t i;
  uint8_t match = 0;

  for (i = 0; i < nbr_bytes; ++i) {
      match = match | (mem_1[i] ^ mem_2[i]);
  }

  return match;
}

uint8_t g_hmac_key[HMAC_KEY_LENGH];

void initialize_hmac_key(void)
{
	uint8_t hmac_key[HMAC_KEY_LENGH];
	size_t hmac_key_len = sizeof(hmac_key);
	
#ifdef USE_TRUSTONIC
	// Note that Keymaster must derive the same key.
    const uint8_t salt[] = {'k','e','y','m','a','s','t','e','r','1', 0, 0};
    if (TLAPI_OK != tlApiDeriveKey(
        salt, sizeof(salt),
        hmac_key, hmac_key_len,
        MC_SO_CONTEXT_SP, /* MC_SPID_SYSTEM */
        MC_SO_LIFETIME_POWERCYCLE))
    {
		EMSG("%s:%d  failed ", __func__, __LINE__);
        return;
    }
#else
	TEE_GenerateRandom(hmac_key, hmac_key_len);
#endif
	
	memcpy(g_hmac_key, hmac_key, sizeof(hmac_key));
}

void get_hmac_key(uint8_t *hmac_key)
{
	memcpy(hmac_key, g_hmac_key, sizeof(g_hmac_key));
}

int tt_check_token_integrity(hw_auth_token_t* token)
{
	uint8_t hmac_key[HMAC_KEY_LENGH];
	uint8_t hmac[HMAC_KEY_LENGH];
	TEE_Result res = TEE_SUCCESS;
	
	get_hmac_key(hmac_key);

    if (token->version != 0) {
        EMSG("%s:%d  token->version=%d failed ", __func__, __LINE__, token->version);
		return TEE_ERROR_BAD_PARAMETERS;
    }

    res = tt_hmac_sha256(
                  (uint8_t*) token, sizeof(*token) - sizeof(token->hmac),
                   hmac_key, sizeof(hmac_key), hmac);

    if (res) {
        EMSG("%s:%d  res=0x%x(%d) failed ", __func__, __LINE__, res, res);
		return res;
    }

    res = tt_crypto_memcmp(hmac, token->hmac, sizeof(hmac));
    if (res) {
        EMSG("%s HMAC doesn't match", __func__);
        EMSG("%s:%d  res=0x%x(%d) HMAC doesn't match", __func__, __LINE__, res, res);
		return res;
    }

    return 0;
}

const char * base64char = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
char * base64_encode( char * bindata, size_t binlength , char * base64)
{
    size_t i, j;
    unsigned char current;

    for ( i = 0, j = 0 ; i < binlength ; i += 3 )
    {
        current = (bindata[i] >> 2) ;
        current &= (unsigned char)0x3F;
        base64[j++] = base64char[(int)current];

        current = ( (unsigned char)(bindata[i] << 4 ) ) & ( (unsigned char)0x30 ) ;
        if ( i + 1 >= binlength )
        {
            base64[j++] = base64char[(int)current];
            base64[j++] = '=';
            base64[j++] = '=';
            break;
        }
        current |= ( (unsigned char)(bindata[i+1] >> 4) ) & ( (unsigned char) 0x0F );
        base64[j++] = base64char[(int)current];

        current = ( (unsigned char)(bindata[i+1] << 2) ) & ( (unsigned char)0x3C ) ;
        if ( i + 2 >= binlength )
        {
            base64[j++] = base64char[(int)current];
            base64[j++] = '=';
            break;
        }
        current |= ( (unsigned char)(bindata[i+2] >> 6) ) & ( (unsigned char) 0x03 );
        base64[j++] = base64char[(int)current];

        current = ( (unsigned char)bindata[i+2] ) & ( (unsigned char)0x3F ) ;
        base64[j++] = base64char[(int)current];
    }
    base64[j] = '\0';
    return base64;
}


